#ifndef GPIO_HPP_
#define GPIO_HPP_


#include "main.h"



class GPIO
{
public:

	struct Config
	{
		GPIO_TypeDef *PORT;
		int Pin;
		int mode;
		int output_type;
		int speed;
		int pull;
		int edge;
		int alternate_function;
	}Config;

	enum class Mode
	{
		Input = 0,
		Output = 1,
		Alternate = 2,
		Analog = 3,
	};

	enum class Output_Type
	{
		Push_Pull = 0,
		Open_Drain = 1,
	};

	enum class Speed
	{
		Low_Speed = 0,
		Medium_Speed = 1,
		High_Speed = 2,
		Very_High_Speed = 3,
	};

	enum class Pull
	{
		UP = 1,
		Down = 2,
	};

	enum class Edge{
		Rising = 0,
		Falling = 1,
		Both = 2,
	};

	enum class Alternate_Function
	{
/*********************************************************************************************/
		MCO1 = 0,
		MC02 = 0,
		SWDIO = 0,
		SWCLK = 0,
		JTDI = 0,
		JTDO = 0,
		NJRST = 0,
		RTC_REFIN = 0,
		TRACECLK = 0,
		TRACED0 = 0,
		TRACED1 = 0,
		TRACED2 = 0,
		TRACED3 = 0,
/*********************************************************************************************/
		TIM_1_2 = 1,
/*********************************************************************************************/
		TIM_3_4_5 = 2,
/*********************************************************************************************/
		TIM_8_9_10_11 = 3,
/*********************************************************************************************/
		I2C_1_2_3 = 4,
/*********************************************************************************************/
		SPI_1_2 = 5,
		I2S_2_ext = 5,
/*********************************************************************************************/
		SPI_3 = 6,
		I2S_3_ext = 6,
/*********************************************************************************************/
		USART_1_2_3 = 7,
/*********************************************************************************************/
		USART_4_5_6 = 8,
/*********************************************************************************************/
		CAN_1_2 = 9,
		TIM_12_13_14 = 9,
/*********************************************************************************************/
		OTG_FS_HS = 10,
/*********************************************************************************************/
		ETH_ = 11,
/*********************************************************************************************/
		FSMC_SDIO_OTG_FS = 12,
/*********************************************************************************************/
		DCMI_ = 13,
/*********************************************************************************************/
	};


	void Init(void);
	void Write(bool bit);
	int  Read(void);
	void Interrupt_Setup(void);




private:
};



#endif /* GPIO_HPP_ */
